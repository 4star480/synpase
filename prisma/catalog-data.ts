import { draftListing } from "./market-prices";

const CATEGORIES = ["CURRENCY", "ACCOUNT", "ITEM", "BOOSTING"] as const;

export function generateCatalogListings(targetCount = 3200) {
  const listings: {
    gameSlug: string;
    category: string;
    title: string;
    description: string;
    price: number;
    stock: number;
    deliveryMins: number;
    featured: boolean;
    views: number;
  }[] = [];

  let idx = 0;
  while (listings.length < targetCount) {
    for (const game of GAMES) {
      for (const category of CATEGORIES) {
        const item = draftListing(game, category, idx);
        listings.push({
          gameSlug: game.slug,
          category,
          title: item.title,
          description: item.description,
          price: item.price,
          stock: item.stock,
          deliveryMins: item.deliveryMins,
          featured: listings.length % 47 === 0,
          views: 120 + Math.floor(Math.random() * 2800),
        });
        idx++;
        if (listings.length >= targetCount) break;
      }
      if (listings.length >= targetCount) break;
    }
  }
  return listings;
}
